from typing import Any
from pygame import *
from random import randint

window = display.set_mode((700, 500))
display.set_caption("Шутер")
background = transform.scale(image.load("galaxy.jpg"), (700, 500))
clock = time.Clock()
FPS = 60

class GameSprite(sprite.Sprite):
    def __init__(self, player_image, x, y, width, height, player_speed):
        super().__init__()
        self.image = transform.scale(image.load(player_image), (width, height))
        self.speed = player_speed
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
    def reset(self):
        window.blit(self.image, (self.rect.x, self.rect.y))

class Player(GameSprite):
    def update(self):
        keys_pressed = key.get_pressed()
        if keys_pressed[K_a] and self.rect.x > 5:
           self.rect.x -= self.speed
        if keys_pressed[K_d] and self.rect.x < 595:
           self.rect.x += self.speed
    def fire(self):
        bullet = Bullet('bullet.png', self.rect.centerx, self.rect.y, 10, 30, -15)
        bullets.add(bullet)


class Enemy(GameSprite):
    def update(self):
        global lost, score
        self.rect.y += self.speed
        if self.rect.y > 500:
            self.rect.y = 0
            self.rect.x = randint(80, 620)
            lost += 1
            print(lost)
        if sprite.spritecollide(self, bullets,  True):
            score += 1
            self.rect.y = 0
            self.rect.x = randint(80, 620)


class Bullet(GameSprite):
    def update(self):
        self.rect.y += self.speed
        if self.rect.y < 0:
            self.kill()        


lost = 0
score = 0
font.init()

font= font.Font(None, 50)


rocket = Player("rocket.png", 315, 385, 65, 80, 7)

# enemy = Enemy('ufo.png', randint(80, 620), 0, 80, 50, randint(3,10))

enemies = sprite.Group()
for i in range (1,6):
    enemy = Enemy('ufo.png', randint(80, 620), 0, 80, 50, randint(3,5))
    enemies.add(enemy)

bullets = sprite.Group()

win = font.render('YOU WIN', True, (255, 225, 0))


mixer.init()
